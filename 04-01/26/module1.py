# class is a blueprint for creating objects
# object is an instance of a class
# class can have attributes and methods
# object can have attributes and methods too
# method is a function defined inside a class
# attribute is a variable defined inside a class
# self is a reference to the current instance of the class
# __init__ is a special method that is called when an object is created
# class can inherit from another class
# object can inherit from another object too
# inheritance is a way to create a new class from an existing class
# polymorphism is the ability to use a single interface for different data types
# encapsulation is the bundling of data and methods that operate on that data within a single unit
# abstraction is the concept of hiding the complex implementation details and showing only the essential features of the object
# class variables are shared among all instances of a class
# instance variables are unique to each instance of a class
